function nhaptentuoi()
{
    var ten,tuoi;
    ten=prompt("ban hay nhap ten");
    if(ten==null)
    {
        document.write("ban da khong nhap ten")
    }
    else{
        document.write("ten cua ban la:<u>"+ ten+"</u><br>")
    }

    tuoi=prompt("ban hay nhap tuoi");
    if(tuoi==null)
    {
        document.write("ban da khong nhap tuoi")
    }
    else{
        document.write("tuoi cua ban la:<b>"+ tuoi+"</b>")
    }
  
  
   
  
    
  
    
  
 }